void main()
{
  int numOne = 50;
  int numTwo = 30;
  
  //Arithmatic operations
  
  //Addition
  print("50 + 30 = ${numOne + numTwo}");
  
  //substraction
  print("50 - 30 = ${numOne - numTwo}");
  
  //multiplication
  print("50 * 30 = ${numOne * numTwo}");
  
  //division
  print("50 ~/ 30 = ${numOne ~/ numTwo}");
  
  //moduls
  print("50 % 30 = ${numOne % numTwo}"); 
}

