void main()
{
 
  //Logical Operator
  int numOne = 50;
  int numTwo = 30;
  dynamic numThree;
  
  // Logical And (&&)
  numThree = numOne < 70 && numTwo > 70;
  print("condition is = $numThree");
  
  // Logical Or (||)
  numThree = numOne < 70 || numTwo > 70;
  print("condition is = $numThree");
  
}

