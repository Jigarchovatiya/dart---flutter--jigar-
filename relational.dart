void main()
{
 
  int numOne = 50;
  int numTwo = 30;
  
  // Arithmatic operations
  
  // less than(<)
  print("numOne is less than numTwo = ${numOne < numTwo}");
  
  // Greater than(>)
  print("numOne is Greater than numTwo = ${numOne > numTwo}");
  
  // less than equal to(<=)
  print("numOne is less than equal to numTwo = ${numOne <= numTwo}");
  
  // greater than equal to(>=)
  print("numOne is greater than equal to than numTwo = ${numOne >= numTwo}");
  
  // equal to(==)
  print("numOne is equal to numTwo = ${numOne == numTwo}");
  
  // Not Equal To(!=) 
  print("numOne is not equal to numTwo = ${numOne != numTwo}");
  
}

